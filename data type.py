# Datove typy
## STRING = text
pozdrav = "Ahoj, Svete"
print(pozdrav)
print(type(pozdrav))

## INTEGER = cele cislo 
rok = 2025
print(rok)
print(type(rok))

## FLOAT = desatinne cislo
prumerna_znamka = 3.5
print(prumerna_znamka)
print(type(prumerna_znamka))

## BOOLEAN = True/False
Doucovani = True
if (Doucovani == True):
  print("Od 18h budes jeste delat skolu")
else:
  print("Muzes si zahrat na PC")
print(type(Doucovani))

## LIST = seznam
Znamky_z_testu = [1,2,3,4,5]
Predmety_ve_skole = ["Matematika", "Fyzika", "Chemie", "Biologie", "Anglictina"]
print(Znamky_z_testu)
print(Predmety_ve_skole)
print(type(Znamky_z_testu))
print(type(Predmety_ve_skole)) 